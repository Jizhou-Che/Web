CREATE TABLE room(
	roomNo SMALLINT UNSIGNED, -- 0, 1, 1013, 65535
	floor TINYINT UNSIGNED NOT NULL, -- 0, 1, 10, 255
	type VARCHAR(32) NOT NULL,
	CONSTRAINT roomPK PRIMARY KEY (roomNo)
)CHARACTER SET utf8 COLLATE utf8_bin;

CREATE TABLE booking(
	username VARCHAR(64) NOT NULL,
	passport VARCHAR(32) NOT NULL,
	roomNo SMALLINT UNSIGNED NOT NULL,
	checkInDate DATE NOT NULL,
	checkOutDate DATE NOT NULL,
	CONSTRAINT bookingPK PRIMARY KEY (roomNo, checkInDate),
	CONSTRAINT bookingFKuser FOREIGN KEY (username) REFERENCES user (username) ON UPDATE CASCADE,
	CONSTRAINT bookingFKroom FOREIGN KEY (roomNo) REFERENCES room (roomNo) ON UPDATE CASCADE
)CHARACTER SET utf8 COLLATE utf8_bin;

